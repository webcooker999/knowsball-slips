function getGMT4Date(): Date {
  const localDate = new Date();
  // Get UTC time in milliseconds
  const utc = localDate.getTime() + localDate.getTimezoneOffset() * 60000;
  // Convert to GMT-4 (Eastern Time offset in June/Daylight Saving is -4 hours)
  return new Date(utc + (3600000 * -4));
}

// Returns exactly 10 picks based on the current date, resetting at 00:00 local time
export function getDailyBets(): PropBet[] {
  const todayNY = getGMT4Date();
  
  // Calculate day difference relative to start date (June 11, 2026)
  const todayStart = new Date(todayNY.getFullYear(), todayNY.getMonth(), todayNY.getDate());
  const tournamentStart = new Date(2026, 5, 11); // June is month 5 (0-indexed). Aligned to June 11 so matches display on their actual scheduled date
  
  const diffTime = todayStart.getTime() - tournamentStart.getTime();
  const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));
  
  let dayIndex = 0;
  let targetDate = new Date(todayStart);
  
  if (diffDays < 0) {
    // Before the tournament starts, show Day 1 (June 11)
    dayIndex = 0;
    targetDate = new Date(2026, 5, 11);
  } else {
    // During or after the tournament, wrap around the 10 days of bets
    dayIndex = diffDays % 10;
    // targetDate is the actual date mapped to this day index: June 11 + dayIndex
    targetDate = new Date(2026, 5, 11 + dayIndex);
  }
  
  const startIndex = dayIndex * 10;
  const slicedBets = MASTER_BETS.slice(startIndex, startIndex + 10);
  
  // Format targetDate as YYYY-MM-DD
  const year = targetDate.getFullYear();
  const month = String(targetDate.getMonth() + 1).padStart(2, "0");
  const day = String(targetDate.getDate()).padStart(2, "0");
  const dateStr = `${year}-${month}-${day}`;
  
  // Return bets with the play date dynamically attached
  return slicedBets.map(bet => ({
    ...bet,
    date: dateStr,
  }));
}
